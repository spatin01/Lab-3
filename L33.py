import turtle
def color(x):
    greg=turtle.Turtle()
    greg.color(x)
    greg.forward(100)
    greg.right(90)
    greg.forward(100)
    greg.right(90)
    greg.forward(100)
    greg.right(90)
    greg.forward(100)

color("blue")
color("cyan")
color("green")
color("gold")
